    candle_date           DATE,
    candle_duration       VARCHAR,
    open                  DOUBLE,
    high                  DOUBLE,
    low                   DOUBLE,
    close                 DOUBLE,
    volume                DOUBLE,
    tick_count            BIGINT,
    ma_7                  DOUBLE,
    ma_20                 DOUBLE,
    ma_50                 DOUBLE,
    processing_timestamp  VARCHAR
)
WITH (
    location = 's3a://gold/gold_ohlcv',
